// 3.사과를 바구니에 나누어 담으려고 한다. 
// (1)하나의 바구니는 사과를 10개씩 담을 수 있으며  
// (2)사과를 담다가 10개 미만으로 남는경우 하나의 바구니를 추가로 사용해야 한다. 
// 예를 들어 120개의 사과를 나누어 담기 위해서는 12개의 바구니가 필요하지만 / 121~130개까지 사과가 있다면 13개의 바구니가 필요하고 
// 다시 131개의 사과를 나누어 담기 위해서는 14개의 바구니가 필요하게 된다.
// (3)현재 갖고 있는 사과의 수를 의미하는 numOfApples 변수에 123이라는 값이 할당되어 있을 경우 필요한 바구니의 수를 구하는 프로그램을 구현하시오.


// numOfApples 변수는 담아야 할 사과의 총 개수를 저장합니다. 여기서는 123개의 사과가 있습니다.
const numOfApples = 123;

// 10개씩 담았을 경우 남는 사과가 몇개인지 구함 
// numOfApples % 10은 123을 10으로 나눈 나머지를 계산합니다. 즉, 123을 10으로 나누면 12개의 바구니에 담고 나머지 3개의 사과가 남습니다. 그래서 apple에는 3이 저장됩니다.
// console.log는 나머지인 3을 출력합니다.
const apple = numOfApples % 10;
console.log(`나머지=${apple}`);

// 나머지 담기 위한 바구니의 수
// apple == 0 ? 0 : 1은 삼항 연산자를 사용하여, 나머지가 0이면 추가 바구니가 필요 없으므로 0을 할당하고, 나머지가 있으면 추가 바구니가 필요하므로 1을 할당합니다.
// 여기서 apple이 3이므로, 나머지가 0이 아니기 때문에 extrabasket에 1이 할당됩니다.
// console.log는 추가 바구니 수인 1을 출력합니다.
const extrabasket = apple == 0 ? 0 : 1;
console.log(`바구니 수 = ${extrabasket}`);

// 10개씩 나누어 담았을 때 바구니의 수
// (numOfApples - apple) / 10은 사과를 10개씩 담았을 때 필요한 바구니의 수를 계산합니다. 남은 사과를 제외한 120개의 사과를 10개씩 담으므로 12개의 바구니가 필요합니다.
// + extrabasket은 나머지 사과를 담기 위한 추가 바구니의 수를 더합니다. 앞서 계산한 추가 바구니 수는 1입니다.
// 그래서 전체 바구니 수는 12 (기본 바구니 수) + 1 (추가 바구니) = 13이 됩니다.
// console.log는 총 바구니 수인 13을 출력합니다.
let basketcount = (numOfApples - apple) / 10 + extrabasket;
console.log (`바구니 수 =${basketcount}`);
