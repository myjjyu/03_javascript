//변수의 선언
var myNumber1;

//변수의 할당
myNumber1 = 100;

//값
console.log(myNumber1);

//변수의 선언과 할당 통합_ var는 중복선언 가능함
var myNumber2;
myNumber2 = 123;
console.log(myNumber2);

myNumber2 = 456;
console.log(myNumber2);

//변수의 재선언
var myNumber3 = 300;
console.log(myNumber3);

var myNumber3 = 789;
